    public void addTrains() {
        model.addColumn("Nombre");
        model.addColumn("id");
        model.addColumn("kilometros");
        model.addColumn("capacidad de carga");
        model.addColumn("marca");
        model.addColumn("Disponibilidad");
        reloadTable();
    }

    private void reloadTable() {
        //borrar los elementos del modelo
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        list = upb.sgttp.model.domain.Main.getTrains();
        for (int i = 0; i < list.size(); i++) {
//            Train train = list.get(i).getPerson;
            Object u[] = new Object[6];
            u[0] = list.get(i).getTrainName();
            u[1] = list.get(i).getTrainId();
            u[2] = list.get(i).getKilometers();
            u[3] = list.get(i).getLoadingCapacity();
            u[4] = list.get(i).getBrand();
            u[5] = list.get(i).isAvailable();
            model.addRow(u);
        }
        jTable1.setModel(model);
    }

