    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        // Verificar si se ha seleccionado una fila
        if (selectedRow != -1) {
            // Eliminar la fila del modelo de la tabla
            if (list.size() > 1) {
                list.remove(list.get(selectedRow));
                reloadTable();
            }
        }
        upb.sgttp.model.domain.Main.modifyListTrains(list);
        upb.sgttp.model.domain.Main.modifyJson(list);
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        // Verificar si se ha seleccionado una fila
        if (selectedRow != -1 && list.get(selectedRow).isAvailable()) {
            // modificar la fila del modelo de la tabla
            list.get(selectedRow);
            String sType = (String) jComboBox1.getSelectedItem();
            list = upb.sgttp.model.domain.Main.getTrains();
            String nombre = jTextField1.getText();
            int vagonesPasajeros = Integer.parseInt(jTextField2.getText());
            if (!jTextField2.getText().isBlank()) {
                list.get(selectedRow).setTrainName(nombre);
                list.get(selectedRow).setwagons(vagonesPasajeros);
                upb.sgttp.model.domain.Main.modifyListTrains(list);
                upb.sgttp.model.domain.Main.modifyJson(list);
                reloadTable();
                jTextField1.setText("");
                jTextField2.setText("");
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

